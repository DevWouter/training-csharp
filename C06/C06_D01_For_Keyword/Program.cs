// This program counts up from 0 to 9

//  ┌ 🆕 keyword (not a function). It requires three statements: Initializer-, condition- and increment-statement.
//  │ ┌ Initializer-statement: `int i = 0` is executed once before the loop starts
//  │ │ ┌ Condition-statement: `i < 10` is checked before each iteration
//  │ │ │ ┌ Increment-statement: `i++` is executed after each iteration
    for(int i = 0; i < 10; i++)
    {
        Console.WriteLine(i);
    }
