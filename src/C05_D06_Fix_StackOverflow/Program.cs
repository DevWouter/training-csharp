// This example will print all the numbers but won't crash. 

int counter = 0;

// ┌ while keyword 
// │  ┌ Start of the while condition
// │  │ ┌ A boolean expression that when false stops the loop
// │  │ │  ┌ End of the while condition 
while (true) 
{ 
    Console.WriteLine(counter);
    counter++;
}